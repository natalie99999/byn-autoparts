// ── Apply translations to page
function applyLang(code) {
  const t = LANGS[code];
  if (!t) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.innerHTML = t[key];
      }
    }
  });
  localStorage.setItem('byn_lang', code);
  // Update active button
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === code);
  });
}

// ── Language switcher UI
function buildLangSwitcher() {
  const saved = localStorage.getItem('byn_lang') || 'EN';
  const wrap = document.createElement('div');
  wrap.className = 'lang-switcher';
  [['EN','EN'],['ZH','中文'],['RU','РУС'],['ES','ES']].forEach(([code, label]) => {
    const b = document.createElement('button');
    b.className = 'lang-btn' + (code === saved ? ' active' : '');
    b.textContent = label;
    b.dataset.lang = code;
    b.onclick = () => applyLang(code);
    wrap.appendChild(b);
  });
  return wrap;
}

// Global translation helper for JS-rendered content
window.T = (key) => (LANGS[localStorage.getItem('byn_lang') || 'EN'] || LANGS.EN)[key] || key;

document.addEventListener('DOMContentLoaded', () => {
  const navInner = document.querySelector('.nav-inner');
  if (navInner) navInner.appendChild(buildLangSwitcher());
  const saved = localStorage.getItem('byn_lang') || 'EN';
  applyLang(saved);
});
